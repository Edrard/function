/**
* Indent Json befor save
*
* @param mixed $json
*/
if (! function_exists('json_indent')) {
    function json_indent($json)
    {
        $result      = '';
        $pos         = 0;
        $strLen      = strlen($json);
        $indentStr   = '  ';
        $newLine     = "\n";
        $prevChar    = '';
        $outOfQuotes = true;

        for ($i=0; $i<=$strLen; $i++) {

            // Grab the next character in the string.
            $char = substr($json, $i, 1);

            // Are we inside a quoted string?
            if ($char == '"' && $prevChar != '\\') {
                $outOfQuotes = !$outOfQuotes;

                // If this character is the end of an element,
                // output a new line and indent the next line.
            } elseif (($char == '}' || $char == ']') && $outOfQuotes) {
                $result .= $newLine;
                $pos --;
                for ($j=0; $j<$pos; $j++) {
                    $result .= $indentStr;
                }
            }

            // Add the character to the result string.
            $result .= $char;

            // If the last character was the beginning of an element,
            // output a new line and indent the next line.
            if (($char == ',' || $char == '{' || $char == '[') && $outOfQuotes) {
                $result .= $newLine;
                if ($char == '{' || $char == '[') {
                    $pos ++;
                }

                for ($j = 0; $j < $pos; $j++) {
                    $result .= $indentStr;
                }
            }

            $prevChar = $char;
        }

        return $result;
    }
}
/**
* Check if string is json
*
* @param string $string
*/
if (! function_exists('is_json')) {
    function is_json($string)
    {
        json_decode($string);
        return (json_last_error() == JSON_ERROR_NONE);
    }
}
/**
* Json Form converter, using if form data sende in json, becouse of post limitation
*
* @param string $string
* @param string $string
* @param string $string
* @param string $string
* @param string $string
*/
if (! function_exists('json_form_converter')) {
    function json_form_converter( array $data, Closure $func, $name = 'name', $value = 'value'){
        $ready = array();
        foreach($data as $val){
            list( ,$mark,$key) = $func($val[$name]);
            $ready[$key][$mark] = $val[$value];
        }
        return $ready;
    }
}